fun main() {
    //While Loop

    //while da bir döngü çeşididir. diğerlerinden farkı sonu belli değildir
    //sonsuz bi döngü oluşturabiliriz

    var j = 0
    while (j < 10){
        println(j)
        j = j + 1
        //bunu yapmamızın nedeni eğer 1 olarak arttırmazsak
        //sonsuz döngüye girer ve bilgisayarı kitler
        //ayrıca j = j + 1 yerine j++ kullanabilirdik

        //while da koşulu olması lazım
    }


}